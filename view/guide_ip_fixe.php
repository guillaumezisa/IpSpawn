      Un serveur web vous permettera de faire votre site internet,<br>
      Avoir une ip fixe est vraiment une chose importante lors de la configuration d'un serveur.<br>
      On autorise un port à acceder à une ip sur le réseau, si la machine n'est pas statique,<br>
      une autre machine du réseau pourrait prendre sa place et donc, plus de service. <br><br>
      Pour choisir votre ip vous devrez taper la commande "ip a" dans un terminal ,par exemple : <br>
      3: wlan0: <BROADCAST,MULTICAST,UP,LOWER_UP> mtu 1500 qdisc mq state UP group default qlen 1000<br>
      link/ether c8:3d:d4:6f:f1:a7 brd ff:ff:ff:ff:ff:ff<br>
      inet 192.168.43.248/24 brd 192.168.43.255 scope global dynamic noprefixroute wlan0<br><br>
      Ici notre ip est 192.168.43, nous allons donc pouvoir choisir n'importe quel ip dans 192.168.43.xx.<br>
      Vous pouvez remplacer xx par n'importe quel nombre entre 2 et 254.<br><br>
      <h4 style="color:red">
        Attention ! A ne pas mettre l'ip de l'exemple ( vous n'aurez surement pas la même )<br><br>
